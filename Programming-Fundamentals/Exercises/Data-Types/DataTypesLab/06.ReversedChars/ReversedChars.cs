using System;

namespace _06.ReversedChars
{
    class ReversedChars
    {
        static void Main(string[] args)
        {
            char a = Console.ReadLine()[0];
            char b = Console.ReadLine()[0];
            char c = Console.ReadLine()[0];

            Console.WriteLine($"{c} {b} {a}");
        }
    }
}
